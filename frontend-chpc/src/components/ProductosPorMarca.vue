<template>
  <div>
    <HeaderAnth :isAuthenticated="isAuthenticated" @cerrar-sesion="cerrarSesion" />

    <div class="productos-container">
      <div class="home-container">
        <h1 class="animated-title">Productos de {{ nombreMarca }}</h1>
        <p class="animated-subtitle">Explora nuestra selección exclusiva.</p>
        <div class="product-grid">
          <div
            v-for="producto in productos"
            :key="producto.id"
            class="product-card"
          >
            <div class="product-image">
              <img
                :src="producto.imagen_url"
                :alt="producto.nombre_producto"
                loading="lazy"
              />
            </div>
  <h3>{{ producto.nombre_producto }}</h3>
  <p>{{ producto.descripcion }}</p>
  <p class="precio">Precio: ${{ producto.precio }}</p>
  
  <!-- Mostrar stock disponible -->
  <p class="stock">
    <strong>Stock disponible:</strong> {{ producto.stock }} unidades
  </p>

  <button @click="verDetalle(producto.id)">Ver Detalles</button>
</div>

        </div>
      </div>
    </div>

    <FooterAnth />
  </div>
</template>

<script>
import axios from "axios";
import HeaderAnth from "@/components/HeaderAnth.vue";
import FooterAnth from "@/components/FooterAnth.vue";

export default {
  name: "ProductosPorMarca",
  components: {
    HeaderAnth,
    FooterAnth,
  },
  data() {
    return {
      productos: [],
      nombreMarca: "",
      isAuthenticated: false,
    };
  },
  async created() {
    this.isAuthenticated = !!localStorage.getItem("access_token");
    const marcaId = this.$route.params.id;

    try {
      // Obtener productos de la marca
      const response = await axios.get(`http://localhost:5000/tienda/productos/marca/${marcaId}`);
      this.productos = response.data.map((producto) => ({
        ...producto,
        imagen_url: producto.media.length
          ? `http://localhost:5000${producto.media[0].url}`
          : "http://localhost:5000/static/images/default.png",
      }));

      // Obtener información de la marca
      const marcaResponse = await axios.get(`http://localhost:5000/marcas/${marcaId}`);
      this.nombreMarca = marcaResponse.data.nombre_marca;
    } catch (error) {
      console.error("Error al cargar los productos de la marca:", error);
    }
  },
  methods: {
    cerrarSesion() {
      localStorage.removeItem("access_token");
      this.isAuthenticated = false;
      this.$router.replace("/login");
    },
    verDetalle(id) {
      this.$router.push({ name: "ProductoDetalle", params: { id } });
    },
  },
};
</script>

